let first_nmbr = Number(prompt("Please enter your first number", "number"));
let second_nmbr = Number(prompt("Please enter your first number", "number"));
let summ_res = first_nmbr + second_nmbr
let diff_res = first_nmbr - second_nmbr
let mult_res = first_nmbr * second_nmbr
let div_res = first_nmbr / second_nmbr

alert(first_nmbr + "+" + second_nmbr + " = " + summ_res);
alert(first_nmbr + "-" + second_nmbr + " = " + diff_res);
alert(first_nmbr + "*" + second_nmbr + " = " + mult_res);
alert(first_nmbr + "/" + second_nmbr + " = " + div_res);